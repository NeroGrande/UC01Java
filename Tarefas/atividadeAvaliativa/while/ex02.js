/*
Questão 17: Crie uma variável numero e atribua um valor. Usando um while, diminua o valor da 
variável até que ela seja igual a 0, exibindo o valor no console a cada iteração.
*/

let numero = 10; // Atribui um valor à variável

while (numero >= 0) { // Enquanto o número for maior ou igual a 0
  console.log(numero); // Exibe o valor de numero no console
  numero--; // Decrementa o valor de numero em 1 a cada iteração
}
// Fim do programa