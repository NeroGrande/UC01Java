/*
Questão 18: Crie uma variável soma e inicialize com o valor 0. Usando while, 
adicione 2 a soma a cada iteração e pare quando soma atingir o valor 20.
*/

let soma = 0; // Inicializa a variável soma com o valor 0

while (soma < 20) { // Enquanto soma for menor que 20
  soma += 2; // Adiciona 2 à soma
  console.log(soma); // Exibe o valor atual de soma no console
}
