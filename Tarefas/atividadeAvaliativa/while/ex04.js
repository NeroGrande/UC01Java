/*
Questão 19: Crie uma variável numero e atribua o valor 1. Usando while, multiplique 
esse número por 2 até que ele seja maior ou igual a 100, exibindo o número a cada iteração.
*/

let numero = 1; // Inicializa a variável numero com o valor 1

while (numero < 100) { // Enquanto o numero for menor que 100
  console.log(numero); // Exibe o valor atual de numero no console
  numero *= 2; // Multiplica o numero por 2 a cada iteração
}
